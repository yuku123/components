package cube;

public class Point {

    protected Integer x;
    protected Integer y;
    protected Integer z;

    //原始位置坐标
    protected Integer x_point;
    protected Integer y_point;
    protected Integer z_point;


    public Integer getY() {
        return y;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public Integer getZ() {
        return z;
    }

    public void setZ(Integer z) {
        this.z = z;
    }

    public Integer getX_point() {
        return x_point;
    }

    public void setX_point(Integer x_point) {
        this.x_point = x_point;
    }

    public Integer getY_point() {
        return y_point;
    }

    public void setY_point(Integer y_point) {
        this.y_point = y_point;
    }

    public Integer getZ_point() {
        return z_point;
    }

    public void setZ_point(Integer z_point) {
        this.z_point = z_point;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }
}
